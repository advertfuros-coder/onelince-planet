/**
 * Helper function to find nearest hub to a given location
 */
export function findNearestHub(sellerPincode, sellerState) {
  // Find hubs in same state first
  const stateHubs = shippingHubs.filter((hub) => hub.state === sellerState);
  if (stateHubs.length > 0) {
    return stateHubs[0]; // Return first hub in same state
  }

  // Find hubs in same zone
  const sellerHub = shippingHubs.find((hub) => hub.state === sellerState);
  if (sellerHub) {
    const zoneHubs = shippingHubs.filter((hub) => hub.zone === sellerHub.zone);
    if (zoneHubs.length > 0) {
      return zoneHubs[0];
    }
  }

  // Fallback to nearest metro city
  return shippingHubs.find((hub) => hub.tier === "Metro");
}

/**
 * Get hubs by tier
 */
export function getHubsByTier(tier) {
  return shippingHubs.filter((hub) => hub.tier === tier);
}

/**
 * Get total hub count
 */
export function getHubStats() {
  return {
    total: shippingHubs.length,
    metro: shippingHubs.filter((h) => h.tier === "Metro").length,
    tier1: shippingHubs.filter((h) => h.tier === "Tier1").length,
    tier2: shippingHubs.filter((h) => h.tier === "Tier2").length,
  };
}

